package com.tonmedia.tickets.domain;

public class Match {
	private int match_num;
	private String match_home;
	private String match_away;
	private String match_himage;
	private String match_aimage;
	private String match_date;
	private String match_stadium;
	
	
	public int getMatch_num() {
		return match_num;
	}
	public void setMatch_num(int match_num) {
		this.match_num = match_num;
	}
	public String getMatch_home() {
		return match_home;
	}
	public void setMatch_home(String match_home) {
		this.match_home = match_home;
	}
	public String getMatch_away() {
		return match_away;
	}
	public void setMatch_away(String match_away) {
		this.match_away = match_away;
	}
	public String getMatch_himage() {
		return match_himage;
	}
	public void setMatch_himage(String match_himage) {
		this.match_himage = match_himage;
	}
	public String getMatch_aimage() {
		return match_aimage;
	}
	public void setMatch_aimage(String match_aimage) {
		this.match_aimage = match_aimage;
	}
	public String getMatch_date() {
		return match_date;
	}
	public void setMatch_date(String match_date) {
		this.match_date = match_date;
	}
	public String getMatch_stadium() {
		return match_stadium;
	}
	public void setMatch_stadium(String match_stadium) {
		this.match_stadium = match_stadium;
	}
	
	
}
